#include "Basic/FileGroup.h"
#include "GUI/DisplayPrivate.h"
#include "BSP/BSP.h"

#define USE_FPS_TEST 0

/*实例化屏幕对象*/
SCREEN_CLASS screen(
    TFT_PORT_Pin, TFT_RES_Pin, TFT_CS_Pin,
    TFT_RS_Pin, TFT_WR_Pin, TFT_RD_Pin
);

/*实例化页面调度器*/
PageManager page(PAGE_MAX);

/*页面注册器*/
#define PAGE_REG(name)\
do{\
    extern void PageRegister_##name(uint8_t pageID);\
    PageRegister_##name(PAGE_##name);\
}while(0)

#if (USE_FPS_TEST == 1) 
static void Display_FPSTest();
#endif

/**
  * @brief  页面初始化
  * @param  无
  * @retval 无
  */
static void Pages_Init()
{
    PAGE_REG(MainMenu);          //主菜单
    
    page.PagePush(PAGE_MainMenu);//打开表盘
}

extern "C" {
    void lv_demo_benchmark(void);
    void lv_demo_widgets(void);
    void lv_demo_stress(void);
}

/**
  * @brief  显示初始化
  * @param  无
  * @retval 无
  */
void Display_Init()
{
    /*背光关闭*/
    Backlight_Init();
    Backlight_SetValue(0);
    
    /*屏幕初始化*/
    screen.begin();
    screen.setRotation(0);
    screen.fillScreen(screen.Black);
    
#if (USE_FPS_TEST == 1)
    Display_FPSTest();
#endif
    
    /*自动报错初始化*/
    DisplayError_Init();
    
    /*lvgl初始化*/
    lv_init();
    lv_port_disp_init();
    lv_port_indev_init();
    
    /*APP窗口初始化*/
    AppWindow_Creat();
    
    /*页面初始化*/
    Pages_Init();
    
    /*背光渐亮*/
    Backlight_SetGradual(Backlight_GetBKP(), 1000);
    
    //lv_demo_benchmark();
    //lv_demo_stress();
    //lv_demo_widgets();
}

/**
  * @brief  显示更新
  * @param  无
  * @retval 无
  */
void Display_Update()
{
    lv_task_handler();
    page.Running();
}

/**
  * @brief  页面阻塞延时，保持lvgl更新
  * @param  无
  * @retval 无
  */
void PageDelay(uint32_t ms)
{
    uint32_t lastTime = millis();
    while(millis() - lastTime <= ms)
    {
        lv_task_handler();
    }
}

#if (USE_FPS_TEST == 1) 
static void Display_FPSTest()
{
    float Ftime, Ltime;
    float FPS;
    Ftime = millis();
    int i;
    for(i = 100; i > 0; i--)
    {
        screen.fillScreen(screen.Black);
        screen.fillScreen(screen.White);
    }
    Ltime = millis() - Ftime;
    FPS = 100.0f / (Ltime / 1000.0f) * 2.0f;
    screen.fillScreen(screen.Blue);
    screen.setTextSize(1);
    screen.setTextColor(screen.White);
    screen.setCursor(0, 0);
    screen.print(Ltime);
    screen.setCursor(45, 0);
    screen.print("ms");
    screen.setCursor(0, 9);
    screen.print("FPS:");
    screen.setCursor(25, 9);
    screen.print(FPS);
    while(1);
}
#endif
